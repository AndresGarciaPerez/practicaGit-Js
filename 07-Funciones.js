
// Funcion sin retorno
function saludar(nombre) {
  console.log("Hola " + nombre);
}

saludar("Carlos"); // Hola Carlos
saludar("Ana");    // Hola Ana


// Funcion que retorna un valor
function sumar(a, b) {
  return a + b;
}

let resultado = sumar(5, 3);
console.log(resultado); // 8



