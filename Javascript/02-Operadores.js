
// OPERADORES ARITMETICOS

let a = 10, b = 3;

a + b     // 13 (suma)
a - b     // 7  (resta)
a * b     // 30 (multiplicación)
a / b     // 3.333... (división)
a % b     // 1  (módulo o residuo)
a ** b    // 1000 (potencia: 10³)


//OPERADORES DE COMPARACION

5 == "5"     // true (compara valor, no tipo)
5 === "5"    // false (compara valor y el tipo)
5 != "5"     // false ( compara esa declaracion)
5 !== "5"    // true
10 > 5       // true
10 >= 10     // true
7 < 8        // true


